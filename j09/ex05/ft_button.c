int	ft_button(int i, int j, int k)
{
	if ((i <= j && j <= k) || (k <= j && j <= i))
		return (j);
	if ((j <= i && i <= k) || (k <= i && i <= j))
		return (i);
	if ((i <= k && k <= j) || (j <= k && k <= i))
		return (k);
	return (0);
}
